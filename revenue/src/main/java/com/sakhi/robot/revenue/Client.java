package com.sakhi.robot.revenue;

public class Client {
	private String date;
	private String member;
	private String client;
	private int contacvalue;
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public String getMember() {
		return member;
	}
	public void setMember(String member) {
		this.member = member;
	}
	public String getClient() {
		return client;
	}
	public void setClient(String client) {
		this.client = client;
	}
	public int getContacvalue() {
		return contacvalue;
	}
	public void setContacvalue(int contacvalue) {
		this.contacvalue = contacvalue;
	}
	

}
